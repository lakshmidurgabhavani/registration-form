function showInput() {
    document.getElementById('display_user_name').innerHTML =
        document.getElementById("user_name").value;
    document.getElementById('display_user_email').innerHTML =
        document.getElementById("user_email").value;
    document.getElementById('display_user_mobileNo').innerHTML =
        document.getElementById("user_mobileNo").value;
    document.getElementById('display_user_address').innerHTML =
        document.getElementById("user_address").value;
}